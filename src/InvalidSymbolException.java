/**
 * An exception to be used when a CodeBookI object is provided with a symbol it
 * does not handle.
 * @author CIS-121 Staff
 * @version 2.0 - 11/03/11
 */

@SuppressWarnings("serial")
public class InvalidSymbolException extends IllegalArgumentException
{

	public InvalidSymbolException()
	{
		super();
	}

	public InvalidSymbolException(String message)
	{
		super(message);
	}

	public InvalidSymbolException(Throwable cause)
	{
		super(cause);
	}

	public InvalidSymbolException(String message, Throwable cause)
	{
		super(message, cause);
	}

}
